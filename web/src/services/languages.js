/**
 * Get language by code
 */
export function getLanguage(code) {
  return LANGUAGES.find(lang => lang.code === code);
}

/**
 * Get language codes as a comma-separated list for the detection prompt
 */
export function getLanguageCodesForPrompt() {
  return LANGUAGES.map(lang => lang.code).join(', ');
}

/**
 * Build the language detection prompt
 */
export function buildDetectionPrompt(userMessage) {
  const codes = getLanguageCodesForPrompt();
  return `Detect the language of the following text and respond with ONLY the ISO 639-1 language code from this list: ${codes}

If the language is not in the list or you cannot determine it, respond with "en".

Text: "${userMessage}"

Language code:`;
}
